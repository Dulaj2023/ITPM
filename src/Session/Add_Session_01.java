    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        
        if(select_lecturer.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null,  "Select Lecture", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(select_tag.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null,  "Select Tag", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(select_group.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null,  "Select Group", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(selected_subject.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null,  "Select Tag", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(no_of_students.getText().equals(null) || no_of_students.getText().equals("0")){
            JOptionPane.showMessageDialog(null,  "Number of Students Can not be empty!", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(duration.getText().equals(null) || duration.getText().equals("0")){
            JOptionPane.showMessageDialog(null,  "Duration Can not be empty!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
        
        
            String selectlecturer = (String)select_lecturer.getSelectedItem();
            selectlecturer = select_lecturer.getSelectedItem().toString(); 
            String selectTag = (String)select_tag.getSelectedItem();
            selectTag = select_tag.getSelectedItem().toString(); 
            String selectGroup = (String)select_group.getSelectedItem();
            selectGroup = select_group.getSelectedItem().toString(); 
            String selectedSubject = (String)selected_subject.getSelectedItem();
            selectedSubject = selected_subject.getSelectedItem().toString(); 

            int no_of_Students = Integer.valueOf(no_of_students.getText());
            int Duration = Integer.valueOf(duration.getText());


            //SQL query
            String quary = "INSERT INTO session( lec01, subjectName, Stdgroup, tag, Students, duration, lec02)VALUES(?,?,?,?,?,?,?)";

            try {
                ps = DBConnect.getConnection().prepareStatement(quary);

                ps.setString(1, selectlecturer);
                ps.setString(2, selectedSubject);
                ps.setString(3, selectGroup);
                ps.setString(4, selectTag);
                ps.setInt(5, no_of_Students);
                ps.setInt(6, Duration);
                ps.setString(7, selectlecturer);


                if(ps.executeUpdate() == 1){
                    JOptionPane.showMessageDialog(null, "Data Added Successfully");
                    ClearActionPerformed(evt);
                    jTabbedPane1.setSelectedIndex(0);
                }else{
                    JOptionPane.showMessageDialog(null, "Error!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Add_student.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_submitActionPerformed

