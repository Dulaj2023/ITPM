    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try {
            String LecName = L_name.getText();
            String LecID = L_id.getText();
            String Lecfaculty = (String)faculty.getSelectedItem();
            Lecfaculty = faculty.getSelectedItem().toString();
            String LecDepartment = (String)Department.getSelectedItem();
            LecDepartment = Department.getSelectedItem().toString();
            String LecCenter = (String)center.getSelectedItem();
            LecCenter = center.getSelectedItem().toString();
            String LecBuilding = (String)building.getSelectedItem();
            LecBuilding = building.getSelectedItem().toString();
            String LecLevel = (String)level.getSelectedItem();
            LecLevel = level.getSelectedItem().toString();
            String Rank  = rank.getText();
            
            String quary = "INSERT INTO lecture(Name, LectureID, faculty, department, center, building, level,rank)VALUES(?,?,?,?,?,?,?,?)";
            
            ps = DBConnect.getConnection().prepareStatement(quary);
            
            ps.setString(1, LecName);
            ps.setString(2, LecID);
            ps.setString(3, Lecfaculty);
            ps.setString(4, LecDepartment);
            ps.setString(5, LecCenter);
            ps.setString(6, LecBuilding);
            ps.setString(7, LecLevel);
            ps.setString(8, Rank);
            
            
            if(ps.executeUpdate() == 1){
                 JOptionPane.showMessageDialog(null, "Data Added Successfully");
                clearBtnActionPerformed(evt);
            }else{
                JOptionPane.showMessageDialog(null, "Error!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Add_Lecturer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveActionPerformed

