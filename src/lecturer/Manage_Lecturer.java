    private void DetailsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DetailsTableMouseClicked
        DefaultTableModel model = (DefaultTableModel)DetailsTable.getModel();
        int selectdindex = DetailsTable.getSelectedRow();
        try {
            L_id.setText(model.getValueAt(selectdindex, 0).toString());
            String LecID = L_id.getText();
            
            String quary = "Select * From lecture Where LectureID = ?";
            ps = DBConnect.getConnection().prepareStatement(quary);
            ps.setString(1, LecID);
            rs = ps.executeQuery();
            while(rs.next()){
                L_name.setText(rs.getString("Name"));
                
                faculty.setSelectedItem(rs.getString("faculty"));
                department.setSelectedItem(rs.getString("department"));
                center.setSelectedItem(rs.getString("center"));
                building.setSelectedItem(rs.getString("building"));
                level.setSelectedItem(rs.getString("level"));
                rank.setText(rs.getString("rank"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Manage_student.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_DetailsTableMouseClicked

