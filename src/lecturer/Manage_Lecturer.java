    public void LoadTable(){
        try {
            String quary = "Select * From lecture";
            ps = DBConnect.getConnection().prepareStatement(quary);
            rs = ps.executeQuery();
            while(rs.next()){
                String Name = rs.getString("Name");
                String LectureID = rs.getString("LectureID");
                String faculty =rs.getString("faculty");
                String department = rs.getString("department");
                String center = rs.getString("center");
                String building = rs.getString("building");
                String level = rs.getString("level");
                String rank = rs.getString("rank");

                String tbData[] = {LectureID, Name, faculty, center, level};
                DefaultTableModel tblModel = (DefaultTableModel)DetailsTable.getModel();
                
                tblModel.addRow(tbData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Manage_student.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

