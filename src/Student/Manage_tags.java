    public void loadTable(){
        DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
         while(model.getRowCount() > 0)
        {
            model.removeRow(0);
        }

         try {
            String quary = "Select * From tags";
            ps = DBConnect.getConnection().prepareStatement(quary);
            rs = ps.executeQuery();
            while(rs.next()){
                String id = String.valueOf(rs.getInt("id"));
                String TagName = rs.getString("TagName");
                String TagCode =rs.getString("TagCode");
                String relatedTag = rs.getString("relatedTag");

                String tbData[] = {id, TagName, TagCode, relatedTag};
                DefaultTableModel tblModel = (DefaultTableModel)jTable2.getModel();
                
                tblModel.addRow(tbData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Manage_student.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
