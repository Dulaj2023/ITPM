    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        int a=JOptionPane.showConfirmDialog(null,"Are you Sure?");
        if(a==JOptionPane.YES_OPTION){
            try {
                DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
                int selectdindex = jTable2.getSelectedRow();

                int id =Integer.valueOf(model.getValueAt(selectdindex, 0).toString());

                String quary = "DELETE FROM tags where id = ?";
                ps = DBConnect.getConnection().prepareStatement(quary);
                ps.setInt(1, id);

                if(ps.executeUpdate() == 1){
                    JOptionPane.showMessageDialog(null, "Deleted");
                    while(model.getRowCount() > 0)
                    {
                        model.removeRow(0);
                    }
                    loadTable();
                }

            } catch (SQLException ex) {
                Logger.getLogger(Manage_Locations.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else if(a==JOptionPane.NO_OPTION){
            
        }

    }//GEN-LAST:event_deleteActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
        int selectdindex = jTable2.getSelectedRow();
        
        String  id = model.getValueAt(selectdindex, 0).toString();
        
        String TagName = tagName.getText();
        String TagCode = tagCode.getText();
        String Related_tag = (String)relatedTag.getSelectedItem();
        Related_tag = relatedTag.getSelectedItem().toString();

        String quary = "UPDATE tags SET TagName = ? ,TagCode = ? ,relatedTag = ? where id = ?";

        try {
            ps = DBConnect.getConnection().prepareStatement(quary);

            ps.setString(1, TagName);
            ps.setString(2, TagCode);
            ps.setString(3, Related_tag);
            ps.setString(4, id);

            if(ps.executeUpdate() == 1){
                JOptionPane.showMessageDialog(null, "Data Update Successfuly");
                clearActionPerformed(evt);
                
                loadTable();
            }else{
                JOptionPane.showMessageDialog(null, "Error!");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Add_tag.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateActionPerformed

